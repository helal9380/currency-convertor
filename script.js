const dropdown = document.querySelectorAll(".drobdown select")
const btn = document.querySelector(".btn")
const formCurr = document.querySelector(".from select")
const toCurr = document.querySelector(".to select")
const msg = document.querySelector(".msg");
// console.log(btn)

for(let select of dropdown){
    for(countryCode in countryList){
        let newElement = document.createElement("option")
        newElement.innerText = countryCode;
        newElement.value = countryCode;
        select.append(newElement)
        // console.log(countryCode, select)
        if(select.name === "form" && countryCode === "USD"){
            newElement.selected = "selected";
        } else if(select.name === "to" && countryCode === "INR"){
            newElement.selected = "selected";
        }
        // console.log(countryCode, countryList[countryCode])
    }
    select.addEventListener('change', (e) => {
        showFlag(e.target)
    })
}

const showFlag  = (element) => {
    let countryCode = element.value;
    // console.log(currencyCode)
    let code = countryList[countryCode]
    let newscr = `https://flagsapi.com/${code}/flat/64.png`;
    let img = element.parentElement.querySelector('img');
    img.src = newscr;
}
btn.addEventListener("click", async(e) => {
    e.preventDefault()
    let amout = document.querySelector(".amount input");
    let amoutVlue = amout.value;
    if(amoutVlue === "" || amoutVlue < 1){
        amoutVlue = 1;
        amout.value = '1';
    }
    
    const url = `${currenncyRate}/${formCurr.value.toLowerCase()}/${toCurr.value.toLowerCase()}.json`;
    let response = await fetch(url)
    let data = await response.json();
    let rate = data[toCurr.value.toLowerCase()]
    let toFixed = rate.toFixed(2)
    msg.innerText = `${amoutVlue} ${formCurr.value} =  ${toFixed} ${toCurr.value}`;
    // console.log(toFixed)
   
    // console.log(amout)
})